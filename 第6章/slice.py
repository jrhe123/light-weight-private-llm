# 切片的示例

# 定义列表
list = [1, 2, 3, 4, 5, 6, 7, 8, 9]

# 切片
# 切片，从第2个元素开始，到第8个元素结束
print(list[1:8]) #规则：list[起始位置:结束位置]，不包含结束位置的元素

# 完整的规则：list[起始位置:结束位置:步长]
# 从第2个元素开始，到第8个元素结束，步长为2
print(list[1:8:3])

# 示例3
print(list[:5])
print(list[1:])
print(list[:])

# 示例4
print(list[::-1])
str="hello"
print(str[::-1]) # 当步长为负-1时，切片的启始位置和结束位置表示的分别是最后一个元素和第一个元素
print(str[3::-1]) 